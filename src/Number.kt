// INTEGER NUMBER IN KOTLIN (TIPE DATA)
fun main() {
    var age: Byte = 30 // -128 sampai 127
    var height: Int = 100000 // -2,147,483,648 sampai 2,147,483,647
    var distance: Short = 20000 // -32768 sampai 32767
    var balance: Long = 10000000L // -9,223,372,036,854,775,808 sampai 9,223,372,036,854,775,807

    println("balance : $balance")
    println("age : $age")
    println("height : $height")
    println("distance : $distance")

}

